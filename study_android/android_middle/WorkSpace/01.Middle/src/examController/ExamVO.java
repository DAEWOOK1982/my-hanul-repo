package examController;

public class ExamVO {
	 int no  ;
	 String exam_content ;
	 String exam_dap ;
	 int  exam_cnt ;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getExam_content() {
		return exam_content;
	}
	public void setExam_content(String exam_content) {
		this.exam_content = exam_content;
	}
	public String getExam_dap() {
		return exam_dap;
	}
	public void setExam_dap(String exam_dap) {
		this.exam_dap = exam_dap;
	}
	public int getExam_cnt() {
		return exam_cnt;
	}
	public void setExam_cnt(int exam_cnt) {
		this.exam_cnt = exam_cnt;
	}
	 
	 
}
